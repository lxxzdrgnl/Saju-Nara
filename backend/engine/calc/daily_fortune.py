def _get_level(score: int) -> str:
    for threshold, label in _LEVEL_BOUNDS:
        if score >= threshold:
            return label
    return "주의"


def _get_overall(avg: float) -> str:
    for threshold, text in _OVERALL_TEXTS:
        if avg >= threshold:
            return text
    return _OVERALL_TEXTS[-1][1]


def _branch_pair_bonus(today_branch: str, saju_branch: str, *, weight: float = 1.0) -> float:
    """오늘 지지 vs 사주 지지 관계 보너스 (충·합·형·파·해)."""
    pair_fwd = (today_branch, saju_branch)
    pair_bwd = (saju_branch, today_branch)

    for a, b in CHUNG_PAIRS:
        if (a, b) == pair_fwd or (a, b) == pair_bwd:
            return -15 * weight

    for h in YUK_HAP:
        hp = tuple(sorted(h["pair"]))
        if hp == tuple(sorted([today_branch, saju_branch])):
            return 8 * weight

    for a, b in YUK_HAE:
        if (a, b) == pair_fwd or (b, a) == pair_fwd:
            return -6 * weight

    for a, b in PA_PAIRS:
        if (a, b) == pair_fwd or (b, a) == pair_fwd:
            return -8 * weight

    for _, members in SAM_HYEONG.items():
        if today_branch in members and saju_branch in members and today_branch != saju_branch:
            return -10 * weight

    return 0.0


def _wuxing_bonus(today_branch_element: str, yong_sin: dict) -> int:
    primary = yong_sin.get("primary", "")
    xi_sin  = yong_sin.get("xi_sin", [])
    ji_sin  = yong_sin.get("ji_sin", [])

    if today_branch_element == primary:
        return 15
    if today_branch_element in xi_sin:
        return 10
    if today_branch_element in ji_sin:
        return -10
    return 0


def _compress_score(raw: float) -> int:
    """점수 압축: 중간(50)에서의 편차를 1.7배 확대하여 고득점/저득점이 더 강조되게."""
    compressed = 50 + (raw - 50) * 1.7
    return max(0, min(100, round(compressed)))


def _clothing_color(yong_sin: dict) -> dict:
    """용신/희신 오행 기반 추천 옷 색깔."""
    primary = yong_sin.get("primary", "")
    xi_sin  = yong_sin.get("xi_sin", [])

    el = primary or (xi_sin[0] if xi_sin else "")
    if not el or el not in _ELEMENT_COLORS:
        return {"color": "밝고 자연스러운 색상", "element": "", "reason": "편안한 색상이 하루를 안정적으로 이끕니다."}

    data = _ELEMENT_COLORS[el]
    return {"color": data["color"], "element": el, "reason": data["reason"]}


